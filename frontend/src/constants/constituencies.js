// Get constituencies for a state
export function getConstituenciesByState(stateName) {
  return LOK_SABHA_CONSTITUENCIES[stateName] || []
}

// Get all constituencies (flattened)
export function getAllConstituencies() {
  return Object.values(LOK_SABHA_CONSTITUENCIES).flat()
}

// Check if a constituency belongs to a state
export function isConstituencyInState(constituency, stateName) {
  const constituencies = getConstituenciesByState(stateName)
  return constituencies.includes(constituency)
}


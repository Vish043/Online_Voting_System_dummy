// Get districts for a state (Vidhan Sabha)
export function getDistrictsByState(stateName) {
  const stateData = VIDHAN_SABHA_CONSTITUENCIES[stateName]
  if (!stateData) return []
  return Object.keys(stateData)
}

// Get constituencies for a district in a state (Vidhan Sabha)
export function getConstituenciesByDistrict(stateName, districtName) {
  const stateData = VIDHAN_SABHA_CONSTITUENCIES[stateName]
  if (!stateData) return []
  return stateData[districtName] || []
}

// Check if a state has Vidhan Sabha data
export function hasVidhanSabhaData(stateName) {
  return !!VIDHAN_SABHA_CONSTITUENCIES[stateName]
}

